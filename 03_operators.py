# operators perform operations upon operand
# operand is the value on which operator is applied  
# operator is the symbol that operates on operand

# Arithmetic Operators- To perform arithmetic operations
# +, -, *, /, //, %, **
# + => addition
# - => subtraction
# * => multiplication
# / => division
# // => floor division
# % => modulus
# ** => exponentiation
val1=369;
val2=258;
sum=val1+val2;
print("sum=", sum);

# modulo operator gives the remainder
print("modulo=", (val1%val2));

# floor division gives the quotient
print("floor division=", (val1//val2));

# ****************************************************************

# Relational / comparision operators-
# >, <, >=, <=, ==, !=
# > => greater than
# < => less than
# >= => greater than or equal to
# <= => less than or equal to
# == => equal to (strict check)
# != => not equal to


# it returns True or False as result

# ****************************************************************

# Assignment operators-
# =, +=, -=, *=, /=, %=, //=, **=
# = => assigns the value on the right side to the variable on the left side
# += => adds the value on the right side to the variable on the left side
# -= => subtracts the value on the right side to the variable on the left side
# *= => multiplies the value on the right side to the variable on the left side
# /= => divides the value on the right side to the variable on the left side
# %= => modulus the value on the right side to the variable on the left side
# //= => floor division the value on the right side to the variable on the left side
# **= => exponentiation the value on the right side to the variable on the left side
val1=val1%10;
print(val1);
# ****************************************************************

# Logical operators-
# and, or, not
# and => returns True if both the operands are True
# or => returns True if any one of the operands is True
# not => returns True if operand is False

print(not True);
# ****************************************************************
